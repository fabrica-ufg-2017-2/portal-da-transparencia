package fabrica.model.domain.geral;

/**
 * @author gabriel
 * @version 1.0.0
 *
 * Enum que define papel assumido pela pessoa dentro
 * do portal em determinado ponto no tempo, ou seja uma
 * Pessoa que hoje assume papel de Docente amanhã em poderá
 * assumir o papel de Pesquisador.
 */
public enum  Papel {

    //Papel de docente.
    DOCENTE,

    //Papel de dicente.
    DICENTE,

    //Papel técnico.
    TECNICO,

    //Papel pesquisador.
    PESQUISADOR,

    //Papel financidador.
    FINANCIADOR,

    //Papel aturorizador.
    AUTORIZADOR;

}
